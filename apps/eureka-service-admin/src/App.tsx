import React, { useEffect, useState } from "react";
import { Admin, DataProvider, Resource } from "react-admin";
import buildGraphQLProvider from "./data-provider/graphqlDataProvider";
import { theme } from "./theme/theme";
import Login from "./Login";
import "./App.scss";
import Dashboard from "./pages/Dashboard";
import { ServiceInstanceList } from "./serviceInstance/ServiceInstanceList";
import { ServiceInstanceCreate } from "./serviceInstance/ServiceInstanceCreate";
import { ServiceInstanceEdit } from "./serviceInstance/ServiceInstanceEdit";
import { ServiceInstanceShow } from "./serviceInstance/ServiceInstanceShow";
import { jwtAuthProvider } from "./auth-provider/ra-auth-jwt";

const App = (): React.ReactElement => {
  const [dataProvider, setDataProvider] = useState<DataProvider | null>(null);
  useEffect(() => {
    buildGraphQLProvider
      .then((provider: any) => {
        setDataProvider(() => provider);
      })
      .catch((error: any) => {
        console.log(error);
      });
  }, []);
  if (!dataProvider) {
    return <div>Loading</div>;
  }
  return (
    <div className="App">
      <Admin
        title={"Eureka Service"}
        dataProvider={dataProvider}
        authProvider={jwtAuthProvider}
        theme={theme}
        dashboard={Dashboard}
        loginPage={Login}
      >
        <Resource
          name="ServiceInstance"
          list={ServiceInstanceList}
          edit={ServiceInstanceEdit}
          create={ServiceInstanceCreate}
          show={ServiceInstanceShow}
        />
      </Admin>
    </div>
  );
};

export default App;
